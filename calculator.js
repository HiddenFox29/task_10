(function () {
    window.substract = function(number1, number2) {
        return number1 - number2;
    };
})();

(function () {
    window.add = function(number1, number2) {
        return number1 + number2;
    };
})();

(function () {
    window.multiply = function(number1, number2) {
        return number1 * number2;
    };
})();

(function () {
    window.devide = function(number1, number2) {
        return number1 / number2;
    };
})();